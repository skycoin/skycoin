func BenchmarkDecode(b *testing.B) {
	b.ReportAllocs()
	d, err := Decode(benchmarkAddr)
	if err != nil {
		b.Fail()
	}

	e := Encode(d)
	if e != benchmarkAddr {
		b.Fail()
	}

	b.ResetTimer()

	for i := 0; i < b.N; i++ {
		_ = Encode(d)
	}
}

